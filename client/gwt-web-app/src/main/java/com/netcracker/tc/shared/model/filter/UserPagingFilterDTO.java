package com.netcracker.tc.shared.model.filter;

import java.io.Serializable;

public class UserPagingFilterDTO implements Serializable {

    private Long roleId;
    private String name;
    private String email;
    private String telephone;
    private Boolean hrResultInterview;
    private Boolean devResultInterview;
    private Long positionId;
    private Long userStatusId;

    public UserPagingFilterDTO() {
    }

    public Long getRoleId() {
        return roleId;
    }

    public void setRoleId(Long roleId) {
        this.roleId = roleId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public Boolean getHrResultInterview() {
        return hrResultInterview;
    }

    public void setHrResultInterview(Boolean hrResultInterview) {
        this.hrResultInterview = hrResultInterview;
    }

    public Boolean getDevResultInterview() {
        return devResultInterview;
    }

    public void setDevResultInterview(Boolean devResultInterview) {
        this.devResultInterview = devResultInterview;
    }

    public Long getPositionId() {
        return positionId;
    }

    public void setPositionId(Long positionId) {
        this.positionId = positionId;
    }

    public Long getUserStatusId() {
        return userStatusId;
    }

    public void setUserStatusId(Long userStatusId) {
        this.userStatusId = userStatusId;
    }

    @Override
    public String toString() {
        return "RoleID: " + roleId + ", name: " + name + ", email: " + email + ", tel: " + telephone + ",userStatus " + userStatusId;
    }
}