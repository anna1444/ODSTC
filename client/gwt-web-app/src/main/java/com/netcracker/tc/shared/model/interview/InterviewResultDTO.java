package com.netcracker.tc.shared.model.interview;

import java.io.Serializable;

public class InterviewResultDTO implements Serializable {

    private InterviewSlotDTO interviewSlot;
    private String hr;
    private String interviewer;
    private Boolean isCome;

    private DevInterviewResultDetailDTO devInterviewResultDetail;
    private QAInterviewResultDetailDTO qaInterviewResultDetail;

    public InterviewResultDTO() {
    }

    public InterviewSlotDTO getInterviewSlot() {
        return interviewSlot;
    }

    public void setInterviewSlot(InterviewSlotDTO interviewSlot) {
        this.interviewSlot = interviewSlot;
    }

    public String getHr() {
        return hr;
    }

    public void setHr(String hr) {
        this.hr = hr;
    }

    public String getInterviewer() {
        return interviewer;
    }

    public void setInterviewer(String interviewer) {
        this.interviewer = interviewer;
    }

    public Boolean getIsCome() {
        return isCome;
    }

    public void setIsCome(Boolean isCome) {
        this.isCome = isCome;
    }

    public DevInterviewResultDetailDTO getDevInterviewResultDetail() {
        return devInterviewResultDetail;
    }

    public void setDevInterviewResultDetail(DevInterviewResultDetailDTO devInterviewResultDetail) {
        this.devInterviewResultDetail = devInterviewResultDetail;
    }

    public QAInterviewResultDetailDTO getQaInterviewResultDetail() {
        return qaInterviewResultDetail;
    }

    public void setQaInterviewResultDetail(QAInterviewResultDetailDTO qaInterviewResultDetail) {
        this.qaInterviewResultDetail = qaInterviewResultDetail;
    }
}