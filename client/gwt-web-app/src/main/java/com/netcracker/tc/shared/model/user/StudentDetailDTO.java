package com.netcracker.tc.shared.model.user;

import java.io.Serializable;

public class StudentDetailDTO implements Serializable{

    private static final long DEV_POSITION = 2L;

    private Long id;

    private PositionDTO position;
    private UserStatusDTO userStatus;

    public StudentDetailDTO(Long id, PositionDTO position, UserStatusDTO userStatus) {
        this.id = id;
        this.position = position;
        this.userStatus = userStatus;
    }

    public StudentDetailDTO() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public PositionDTO getPosition() {
        return position;
    }

    public void setPosition(PositionDTO position) {
        this.position = position;
    }

    public UserStatusDTO getUserStatus() {
        return userStatus;
    }

    public void setUserStatus(UserStatusDTO userStatus) {
        this.userStatus = userStatus;
    }
}