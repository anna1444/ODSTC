package com.netcracker.tc.server.persistence.model.interview;

import com.netcracker.tc.server.persistence.model.user.User;

import java.io.Serializable;

/**
 * Created by unconsionable on 28.07.2014.
 */
public class InterviewResult implements Serializable {

    private Long interviewSlotId;

    private User hr;
    private User interviewer;
    private Boolean isCome;

    private DevInterviewResultDetail devInterviewResultDetail;
    private QAInterviewResultDetail qaInterviewResultDetail;

    public InterviewResult() {
    }

    public Long getInterviewSlotId() {
        return interviewSlotId;
    }

    public void setInterviewSlotId(Long interviewSlotId) {
        this.interviewSlotId = interviewSlotId;
    }

    public User getHr() {
        return hr;
    }

    public void setHr(User hr) {
        this.hr = hr;
    }

    public User getInterviewer() {
        return interviewer;
    }

    public void setInterviewer(User interviewer) {
        this.interviewer = interviewer;
    }

    public Boolean getIsCome() {
        return isCome;
    }

    public void setIsCome(Boolean isCome) {
        this.isCome = isCome;
    }

    public DevInterviewResultDetail getDevInterviewResultDetail() {
        return devInterviewResultDetail;
    }

    public void setDevInterviewResultDetail(DevInterviewResultDetail devInterviewResultDetail) {
        this.devInterviewResultDetail = devInterviewResultDetail;
    }

    public QAInterviewResultDetail getQaInterviewResultDetail() {
        return qaInterviewResultDetail;
    }

    public void setQaInterviewResultDetail(QAInterviewResultDetail qaInterviewResultDetail) {
        this.qaInterviewResultDetail = qaInterviewResultDetail;
    }
}